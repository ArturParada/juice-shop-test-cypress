Cypress.Commands.add("selectAllProduct", quantity => {
    cy.xpath('//button[@aria-label="Add to Basket"]').each(($product, index) => {
        cy.wrap($product).click()

    })
    cy.get('span.fa-layers-counter').should("contain", quantity)
})